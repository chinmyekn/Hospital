<h1 style="font-size32px; text-align:center;">Neuro Spine Department</h1>
<img src="docimg/download2.jpg" height="350px" width="400px" style="float:right; margin-top:80px; margin-right:30px; margin-left:30px;border-radius:65px; margin-bottom:20px;">
