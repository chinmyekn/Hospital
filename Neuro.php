<p style="font-size:24px; color:black; font-style:italic; margin-left:30px; text-align:justify;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The Sudharshan Hospital is a national leader in neurological, psychiatric and neurosurgical treatments, offering state-of-the-art care in both paediatrics and adult neurology. Since its inception, this hospital has been a pioneer in neurological research and care.</p>

<p style="font-size:24px; color:black; font-style:italic; margin-left:30px; text-align:justify;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The multidisciplinary team at the Sudharshan Hospital consists of highly skilled and experienced doctors, specialists, nurses and assistants who work diligently to diagnose and treat neurological conditions such as epilepsy, Parkinson’s disease, Alzheimer’s disease, cerebrovascular disease, muscle disorders, degenerative disorders, cerebral stroke, migraine and chronic headache. Besides providing comprehensive care, the team has successfully carried out a number of neurological surgeries, including spinal implant for spasticity.</p>

<p style="font-size:24px; color:black; font-style:italic; margin-left:30px; text-align:justify;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The department offers a range of imaging tests such as CT, MRI, PET and Neurosonology scans to diagnose and treat brain and spine conditions. Other examination procedures include ultrasonography, electroencephalography, electromyography, nerve conduction study and biopsy. For years, Sudharshan Hospital  has been the preferred medical facility for embolisation of aneurysms and arteriovenous malformations (AVMs) as well as neurovascular intervention for acute stroke. This neurological institute is the leader in brain and spine surgery, epilepsy surgery, advanced stroke care, head injury and poly trauma and comprehensive neurological care.</P>
