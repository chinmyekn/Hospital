<h1 style="font-size32px; text-align:center;">Cardiac Department</h1>
<img src="docimg/cardiology-technology_73426-211.jpg" height="350px" width="400px" style="float:right; margin-top:80px; margin-right:30px; margin-left:30px;border-radius:65px; margin-bottom:20px;">
</img>
<p style="font-size:24px; color:black; font-style:italic; margin-left:30px; text-align:justify;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Sudharshan Hospital is the leading department that offers 24/7 comprehensive tertiary care for cardiac complaints and vascular diseases. It is supported by state-of-the-art facilities and a highly skilled team of professionals trained to treat even the most complex cardiac emergencies. The motto behind the Hospital is to offer holistic and affordable treatment for all kinds of cardiac and vascular conditions.</p>

<p style="font-size:24px; color:black; font-style:italic; margin-left:30px; text-align:justify;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The most notable aspect of the Hospital is the easy access to complex adult and paediatric cardiac treatment. This includes surgeries such as heart transplant, surgical intervention for heart failure, implantation of Ventricular Assist Devices (VADs) and many others. The team at Hospital is trained to perform Cardiac Catheterization and its associated procedures, such as Angiogram, Angioplasty, Valvuloplasty, Stenting and Pacemaker Implantation. The institute also offers cutting-edge diagnostic tests like Stress ECG, Myocardial Perfusion Scan, Colour Doppler Echocardiography, Transoesophageal Echocardiogram (TEE) as well as Electrophysiology Studies and Radiofrequency Ablation.</p>
