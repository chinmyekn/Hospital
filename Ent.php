<p style="font-size:24px; color:black; font-style:italic; margin-left:30px; text-allign:justify;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The ENT Department at Sudharshan Hospital is involved with the diagnosis and treatment of diseases, infections and tumours of the ear, nose, throat, head and neck. These areas of the body are affected by certain conditions that can only be diagnosed and treated by specialised doctors. ENT specialists at Sudharshan Hospital are highly skilled and have many years of experience in the treatment and management of these disorders. They work tirelessly to treat patients in a holistic manner to ensure that they are free of symptoms and are well on the path to recovery.</p>

